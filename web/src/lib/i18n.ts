export function translationKeys(locale: UiLocale) {
  return Object.keys(messages[locale]).sort();
}

export function readStoredLocale(): UiLocale {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  try {
    const stored = window.localStorage.getItem(UI_LOCALE_STORAGE_KEY);
    if (stored === "en" || stored === "zh-CN") return stored;
  } catch {
    // Browser privacy settings may make localStorage unavailable.
  }
  const langs = [window.navigator.language, ...(window.navigator.languages || [])]
    .map((lang) => lang.toLowerCase())
    .filter(Boolean);
  if (langs.some((lang) => lang.startsWith("zh"))) return "zh-CN";
  return "en";
}

export function writeStoredLocale(locale: UiLocale) {
  if (typeof window === "undefined") return;
  try {
    window.localStorage.setItem(UI_LOCALE_STORAGE_KEY, locale);
  } catch {
    // The in-memory locale still changes when storage is unavailable.
  }
}

export function translate(locale: UiLocale, key: string, values?: TranslationValues) {
  const template = messages[locale][key] ?? messages.en[key] ?? key;
  if (!values) return template;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name: string) => {
    const value = values[name];
    return value === undefined ? match : String(value);
  });
}

export function formatDateTime(locale: UiLocale, value: string | number | Date) {
  const date = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(date.getTime())) return String(value);
  return new Intl.DateTimeFormat(locale, {
    dateStyle: "medium",
    timeStyle: "short"
  }).format(date);
}

