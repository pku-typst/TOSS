export function localizeClientError(locale: UiLocale, message: string) {
  const normalized = message.trim();
  if (locale === "en" || !normalized) return normalized;
  const exact = zhClientErrorMessages[normalized];
  if (exact) return exact;
  const canvasPage = /^Canvas context is unavailable for Typst page (\d+)$/.exec(normalized);
  if (canvasPage) return `Typst 第 ${canvasPage[1]} 页的 Canvas 上下文不可用`;
  return normalized;
}

export function apiStatusMessage(locale: UiLocale, status: number) {
  if (status === 400) return translate(locale, "api.status.badRequest");
  if (status === 401) return translate(locale, "api.status.unauthorized");
  if (status === 403) return translate(locale, "api.status.forbidden");
  if (status === 404) return translate(locale, "api.status.notFound");
  if (status === 409) return translate(locale, "api.status.conflict");
  if (status >= 500) return translate(locale, "api.status.serverError");
  return translate(locale, "api.status.requestFailed");
}

export function localizeApiErrorDetail(
  locale: UiLocale,
  code: ApiErrorCode | null,
  detail: string | null,
  status: number
) {
  const messageKey = code ? apiErrorMessageKeys[code] : undefined;
  if (messageKey) return translate(locale, messageKey);
  const normalized = detail?.trim() || "";
  if (locale === "en") return normalized || apiStatusMessage(locale, status);
  return zhServerErrorMessages[normalized] || apiStatusMessage(locale, status);
}
